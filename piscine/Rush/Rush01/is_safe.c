int	is_safe(char n, int r, int c, char matrix[][6])
{
	int	i;

	i = 0;
	while (++i < 5)
	{
		if (matrix[r][i] == n)
			return (0);
	}
	i = 0;
	while (++i < 5)
	{
		if (matrix[i][c] == n)
			return (0);
	}
	return (1);
}
