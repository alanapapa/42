int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			j = 1;
		else
			return (0);
		i++;
	}
	return (j);
}
